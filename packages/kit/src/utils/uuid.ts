export function isUUID(input: string) : boolean {
    // eslint-disable-next-line prefer-regex-literals
    const regexp = new RegExp(/^[0-9a-f]{8}-[0-9a-f]{4}-[0-5][0-9a-f]{3}-[089ab][0-9a-f]{3}-[0-9a-f]{12}$/i);
    return regexp.test(input);
}
